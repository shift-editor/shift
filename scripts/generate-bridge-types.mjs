import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";

const repoRoot = resolve(import.meta.dirname, "..");
const inputPath = resolve(repoRoot, "crates/shift-bridge/index.d.ts");
const outputPath = resolve(repoRoot, "packages/types/src/bridge/generated.ts");

const source = readFileSync(inputPath, "utf8");

const idImportMatch = source.match(/import type \{\s*([\s\S]*?)\s*\} from "@shift\/types";\n/);
const headerImports = idImportMatch
  ? idImportMatch[1]
      .split(",")
      .map((name) => name.trim())
      .filter(Boolean)
  : [];

const idTypeNames = new Set([
  "GlyphId",
  "LayerId",
  "PointId",
  "ContourId",
  "AnchorId",
  "AxisId",
  "ComponentId",
  "GuidelineId",
  "SourceId",
]);
const scalarTypeNames = new Set(["GlyphName", "Unicode"]);

const idImports = headerImports.filter((name) => idTypeNames.has(name));
const scalarAliases = headerImports
  .filter((name) => scalarTypeNames.has(name))
  .map((name) => {
    if (name === "GlyphName") {
      return "export type GlyphName = string;";
    }
    if (name === "Unicode") {
      return "export type Unicode = number;";
    }
    return "";
  })
  .filter(Boolean);

function withoutNapiPrefix(text) {
  return text.replace(/\bNapi([A-Z]\w*)\b/g, "$1");
}

function enumToUnion(name, body) {
  const values = [...body.matchAll(/=\s*["']([^"']+)["']/g)].map((match) => `"${match[1]}"`);
  return `export type ${withoutNapiPrefix(name)} = ${values.join(" | ")};\n`;
}

let body = source.replace(idImportMatch?.[0] ?? "", "");

const classMatch = body.match(/export declare class Bridge \{([\s\S]*?)\n\}\n\n/);
if (!classMatch) {
  throw new Error(`Could not find Bridge class declaration in ${inputPath}`);
}

const bridgeMethods = classMatch[1]
  .split("\n")
  .filter((line) => !line.trim().startsWith("constructor("))
  .join("\n")
  .replace(/^  /gm, "  ");

const bridgeApi = `export interface BridgeApi {${withoutNapiPrefix(bridgeMethods)}\n}\n\n`;
body = body.replace(classMatch[0], bridgeApi);

body = body.replace(
  /export declare const enum (Napi[A-Za-z0-9_]+) \{([\s\S]*?)\n\}/g,
  (_match, name, enumBody) => enumToUnion(name, enumBody),
);
body = withoutNapiPrefix(body);
body = body.replace(/\bexport declare /g, "export ");

const imports =
  [idImports.length > 0 ? `import type {\n  ${idImports.join(",\n  ")},\n} from "../ids";\n` : ""]
    .filter(Boolean)
    .join("") + (idImports.length > 0 ? "\n" : "");

const aliases = scalarAliases.length > 0 ? `${scalarAliases.join("\n")}\n\n` : "";

const output =
  `${imports}// This file is generated from crates/shift-bridge/index.d.ts.\n// Run \`pnpm generate:bridge-types\` after rebuilding shift-bridge declarations.\n// Do not edit this file manually.\n\n${aliases}${body}`.replace(
    /\n{3,}/g,
    "\n\n",
  );

mkdirSync(dirname(outputPath), { recursive: true });
writeFileSync(outputPath, output);
